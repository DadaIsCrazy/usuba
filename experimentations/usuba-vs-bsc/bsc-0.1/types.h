#ifndef BSC_TYPES_H__
#define BSC_TYPES_H__

/*
 * We define some integer types; there we can tune between absolute ANSI
 * conformance and maximum performance
 */
typedef unsigned char u8;
typedef signed char s8;
typedef unsigned short u16;
typedef short s16;
typedef unsigned int u32;
typedef int s32;

#endif /* BSC_TYPES_H__ */
